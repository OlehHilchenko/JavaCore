//demonstrate the use of the loop operator for

public class ForTick {
    public static void main(String[] args) {
        int n;
        //for (int n = 10; n > 0; n--) here variable n is announced in for loop
        for (n = 10; n > 0; n--)
            System.out.println("tact " + n);
    }
}
